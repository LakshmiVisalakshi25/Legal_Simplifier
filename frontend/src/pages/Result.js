import React, { useState, useEffect } from 'react';
import axios from 'axios';
import { useParams, useNavigate } from 'react-router-dom';

const API = 'https://legal-simplifier-backend.onrender.com';

export default function Result() {
  const { id } = useParams();
  const navigate = useNavigate();
  const [doc, setDoc] = useState(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetch = async () => {
      const token = localStorage.getItem('token');
      const { data } = await axios.get(`${API}/api/document/${id}`, {
        headers: { Authorization: `Bearer ${token}` }
      });
      setDoc(data);
      setLoading(false);
    };
    fetch();
  }, [id]);

  const downloadReport = () => {
    const riskColor = doc.overallRisk === 'High' ? '#e53e3e' : doc.overallRisk === 'Medium' ? '#d69e2e' : '#38a169';
    const html = `
      <!DOCTYPE html>
      <html>
      <head>
        <meta charset="utf-8">
        <title>Legal Analysis Report — ${doc.fileName}</title>
        <style>
          body { font-family: 'Segoe UI', sans-serif; max-width: 800px; margin: 0 auto; padding: 40px; color: #1a1a2e; }
          h1 { color: #1a1a2e; font-size: 24px; }
          .badge { display: inline-block; background: #eef2ff; color: #667eea; padding: 4px 14px; border-radius: 20px; font-size: 12px; font-weight: 600; margin-bottom: 16px; }
          .summary { color: #4a5568; line-height: 1.7; margin-bottom: 24px; }
          .score { display: inline-block; font-size: 22px; font-weight: 700; color: ${riskColor}; }
          .key-things { background: #fffaf0; border: 1px solid #f6e05e; border-radius: 8px; padding: 16px; margin: 20px 0; }
          .clause { border-left: 4px solid #ccc; padding: 14px 16px; margin-bottom: 14px; border-radius: 4px; }
          .clause-safe { border-left-color: #38a169; background: #f0fff4; }
          .clause-warning { border-left-color: #d69e2e; background: #fffaf0; }
          .clause-danger { border-left-color: #e53e3e; background: #fff5f5; }
          .clause h3 { margin: 0 0 8px; font-size: 14px; }
          .original { font-size: 12px; color: #718096; font-style: italic; margin-bottom: 8px; }
          .simplified { font-size: 13px; color: #2d3748; }
          .footer { margin-top: 40px; text-align: center; color: #a0aec0; font-size: 12px; }
        </style>
      </head>
      <body>
        <h1>⚖️ Legal Analysis Report</h1>
        <div class="badge">${doc.documentType}</div>
        <p class="summary">${doc.summary}</p>
        <p>Overall Risk: <span class="score">${doc.overallRisk}</span> &nbsp;|&nbsp; Danger Clauses: <strong>${doc.dangerCount}</strong> &nbsp;|&nbsp; Warnings: <strong>${doc.warningCount}</strong></p>
        
        ${doc.keyThingsToKnow?.length ? `
        <div class="key-things">
          <strong>⚠️ Key Things To Know Before Signing:</strong>
          <ul>${doc.keyThingsToKnow.map(t => `<li>${t}</li>`).join('')}</ul>
        </div>` : ''}

        <h2>Clause by Clause Breakdown</h2>
        ${doc.clauses.map(c => `
          <div class="clause clause-${c.risk}">
            <h3>${c.title} <span style="font-size:11px;font-weight:400;text-transform:uppercase;">[${c.risk}]</span></h3>
            <div class="original">"${c.original}"</div>
            <div class="simplified">${c.simplified}</div>
          </div>
        `).join('')}

        <div class="footer">Generated by LegalSimplifier · ${new Date().toLocaleDateString('en-IN', { day: 'numeric', month: 'long', year: 'numeric' })}</div>
      </body>
      </html>
    `;
    const blob = new Blob([html], { type: 'text/html' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `Legal_Report_${doc.fileName.replace('.pdf', '')}.html`;
    a.click();
    URL.revokeObjectURL(url);
  };

  if (loading) return (
    <div style={{ minHeight: '80vh', display: 'flex', flexDirection: 'column', alignItems: 'center', justifyContent: 'center' }}>
      <div style={{ width: '48px', height: '48px', border: '4px solid #e2e8f0', borderTopColor: '#667eea', borderRadius: '50%', animation: 'spin 0.8s linear infinite' }}></div>
      <style>{`@keyframes spin { to { transform: rotate(360deg); } }`}</style>
    </div>
  );

  const riskColor = doc.overallRisk === 'High' ? '#e53e3e' : doc.overallRisk === 'Medium' ? '#d69e2e' : '#38a169';
  const riskBg = doc.overallRisk === 'High' ? '#fff5f5' : doc.overallRisk === 'Medium' ? '#fffaf0' : '#f0fff4';

  return (
    <div style={{ maxWidth: '780px', margin: '0 auto', padding: '40px 20px' }}>

      {/* Top action buttons */}
      <div style={{ display: 'flex', gap: '10px', marginBottom: '24px', flexWrap: 'wrap' }}>
        <button
          onClick={() => navigate('/upload')}
          style={{ background: 'transparent', border: '1px solid #667eea', color: '#667eea', padding: '8px 18px', borderRadius: '8px', fontSize: '14px', cursor: 'pointer', fontWeight: '500' }}
        >
          ← Analyze Another
        </button>
        <button
          onClick={downloadReport}
          style={{ background: 'linear-gradient(135deg, #667eea, #764ba2)', border: 'none', color: '#fff', padding: '8px 18px', borderRadius: '8px', fontSize: '14px', cursor: 'pointer', fontWeight: '500' }}
        >
          ⬇ Download Report
        </button>
        <button
          onClick={() => navigate('/history')}
          style={{ background: 'transparent', border: '1px solid #e2e8f0', color: '#718096', padding: '8px 18px', borderRadius: '8px', fontSize: '14px', cursor: 'pointer' }}
        >
          📋 View History
        </button>
      </div>

      {/* Result Header */}
      <div style={{ background: '#fff', borderRadius: '16px', padding: '28px', marginBottom: '20px', boxShadow: '0 2px 8px rgba(0,0,0,0.06)' }}>
        <span style={{ display: 'inline-block', background: '#ebf4ff', color: '#3182ce', padding: '4px 12px', borderRadius: '20px', fontSize: '12px', fontWeight: '600', marginBottom: '12px' }}>
          {doc.documentType}
        </span>
        <h2 style={{ fontSize: '22px', fontWeight: '700', color: '#1a1a2e', marginBottom: '12px' }}>
          Document Analysis Report
        </h2>
        <p style={{ color: '#4a5568', lineHeight: '1.7', fontSize: '14px', marginBottom: '20px' }}>{doc.summary}</p>

        {/* Score row */}
        <div style={{ display: 'flex', gap: '16px', flexWrap: 'wrap' }}>
          <div style={{ background: riskBg, borderRadius: '12px', padding: '16px 24px', textAlign: 'center', minWidth: '100px' }}>
            <div style={{ fontSize: '22px', fontWeight: '700', color: riskColor }}>{doc.overallRisk}</div>
            <div style={{ fontSize: '12px', color: '#718096', marginTop: '2px' }}>Overall Risk</div>
          </div>
          <div style={{ background: '#fff5f5', borderRadius: '12px', padding: '16px 24px', textAlign: 'center', minWidth: '100px' }}>
            <div style={{ fontSize: '22px', fontWeight: '700', color: '#e53e3e' }}>{doc.dangerCount}</div>
            <div style={{ fontSize: '12px', color: '#718096', marginTop: '2px' }}>Danger Clauses</div>
          </div>
          <div style={{ background: '#fffaf0', borderRadius: '12px', padding: '16px 24px', textAlign: 'center', minWidth: '100px' }}>
            <div style={{ fontSize: '22px', fontWeight: '700', color: '#d69e2e' }}>{doc.warningCount}</div>
            <div style={{ fontSize: '12px', color: '#718096', marginTop: '2px' }}>Warnings</div>
          </div>
          <div style={{ background: '#f0fff4', borderRadius: '12px', padding: '16px 24px', textAlign: 'center', minWidth: '100px' }}>
            <div style={{ fontSize: '22px', fontWeight: '700', color: '#38a169' }}>{doc.clauses?.filter(c => c.risk === 'safe').length}</div>
            <div style={{ fontSize: '12px', color: '#718096', marginTop: '2px' }}>Safe Clauses</div>
          </div>
        </div>
      </div>

      {/* Key things */}
      {doc.keyThingsToKnow?.length > 0 && (
        <div style={{ background: '#fffaf0', border: '1px solid #f6e05e', borderRadius: '12px', padding: '20px', marginBottom: '20px' }}>
          <h3 style={{ fontSize: '15px', fontWeight: '600', color: '#744210', marginBottom: '12px' }}>⚠️ Key Things To Know Before Signing</h3>
          <ul style={{ paddingLeft: '20px' }}>
            {doc.keyThingsToKnow.map((t, i) => (
              <li key={i} style={{ fontSize: '13px', color: '#744210', marginBottom: '6px', lineHeight: '1.6' }}>{t}</li>
            ))}
          </ul>
        </div>
      )}

      {/* Clauses */}
      <h3 style={{ fontSize: '18px', fontWeight: '700', color: '#1a1a2e', marginBottom: '16px' }}>📋 Clause by Clause Breakdown</h3>
      {doc.clauses?.map((clause, i) => (
        <div key={i} style={{
          background: '#fff',
          borderRadius: '12px',
          padding: '20px',
          marginBottom: '14px',
          boxShadow: '0 2px 8px rgba(0,0,0,0.06)',
          borderLeft: `4px solid ${clause.risk === 'danger' ? '#e53e3e' : clause.risk === 'warning' ? '#d69e2e' : '#38a169'}`
        }}>
          <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '12px' }}>
            <h4 style={{ fontSize: '15px', fontWeight: '600', color: '#1a1a2e', margin: 0 }}>{clause.title}</h4>
            <span style={{
              padding: '3px 12px',
              borderRadius: '20px',
              fontSize: '11px',
              fontWeight: '600',
              textTransform: 'uppercase',
              background: clause.risk === 'danger' ? '#fff5f5' : clause.risk === 'warning' ? '#fffaf0' : '#f0fff4',
              color: clause.risk === 'danger' ? '#e53e3e' : clause.risk === 'warning' ? '#d69e2e' : '#38a169'
            }}>
              {clause.risk}
            </span>
          </div>
          <div style={{ background: '#f7fafc', borderRadius: '8px', padding: '12px', fontSize: '12px', color: '#718096', lineHeight: '1.6', marginBottom: '12px', fontStyle: 'italic' }}>
            "{clause.original}"
          </div>
          <div style={{ fontSize: '14px', color: '#2d3748', lineHeight: '1.7' }}>{clause.simplified}</div>
        </div>
      ))}
    </div>
  );
}